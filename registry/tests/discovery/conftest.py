def scan_and_wait(api, scan_data, timeout=120):
    """Create a scan and wait for completion."""
    resp = api.post('/discovery/scans', json=scan_data)
    assert resp.status_code == 201, f'Failed to create scan: {resp.text}'
    scan_id = resp.json()['id']

    deadline = time.time() + timeout
    while time.time() < deadline:
        resp = api.get(f'/discovery/scans/{scan_id}')
        assert resp.status_code == 200
        status = resp.json()['status']
        if status in ('completed', 'failed', 'cancelled'):
            return resp.json()
        time.sleep(1)

    raise TimeoutError(f'Scan {scan_id} did not complete within {timeout}s')


