def pytest_collection_modifyitems(config, items):
    """Auto-skip requires_topology tests when topology is not available."""
    if not TOPOLOGY_AVAILABLE:
        skip_topo = pytest.mark.skip(reason='Topology not running (set DISCOVERY_K8S=1 or DISCOVERY_TOPOLOGY_UP=1)')
        for item in items:
            if 'requires_topology' in item.keywords:
                item.add_marker(skip_topo)
